class Solution {
  public:
    long long maxDiamonds(int A[], int N, int K) {
        // code here
        priority_queue<int>v(A, A+N);
        long long  cnt = 0;
        while(K){
            long long m = v.top();
            cnt += m;
            v.pop();
            v.push(m /2);
            K -= 1;
        }
        return cnt;
    }
};


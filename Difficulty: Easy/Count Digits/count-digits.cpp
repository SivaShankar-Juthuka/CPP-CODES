class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int c = N, res = 0;
        while(N) {
            int rem = N % 10;
            if(rem != 0) {
                if(c % rem ==  0) res += 1;
            }
            N /= 10;
        }
        return res;
    }
};


class Solution {
  public:
    vector<long long> factorialNumbers(long long n) {
        // Write Your Code here
        vector<long long>res;
        if(n == 1) return {1};
        long long r = 1, i = 2;
        while(r <= n){
            res.emplace_back(r);
            r *= i;
            i += 1;
        }
        return res;
    }
};


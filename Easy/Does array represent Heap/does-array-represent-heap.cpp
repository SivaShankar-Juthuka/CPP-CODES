class Solution{
    public:
    bool isMaxHeap(int arr[], int n)
    {
        // Your code goes here
        int i = 0, steps = 2, j = 1, k = 2;
        while(j != n){
            if(arr[i] < arr[j]) return false;
            j += 1;
            if(j > steps){
                steps = pow(2, k);
                k += 1;
                i += 1;
            }
        }
        return true;
    }
};


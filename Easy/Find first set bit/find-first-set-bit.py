class Solution:
    
    #Function to find position of first set bit in the given number.
    def getFirstSetBit(self,n):
        #Your code here
        bi = bin(n)[2:]
        bi = list(bi)
        bi = bi[::-1]
        if '1' in bi:
            return bi.index('1')+1
        return 0


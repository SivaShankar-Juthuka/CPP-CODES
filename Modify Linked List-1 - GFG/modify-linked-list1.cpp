class Solution{
    public:
    struct Node* modifyTheList(struct Node *head){
        //add code here.
        vector<int>v;
        Node *tmp = head;
        while(tmp){
            v.emplace_back(tmp->data);
            tmp = tmp->next;
        }
        tmp = head;
        int n = v.size();
        for(int i = 0; i<n/2 ; i++){
            tmp->data = v[n-i-1]- v[i];
            tmp = tmp->next;
        }
        for(int i=n/2;i<n;i++){
            tmp->data = v[n-i-1];
            tmp = tmp->next;
        }
        return head;
    }
};

